void test_atoi()
{
	printf("------------------------------------------------------- FT_ATOI\n");
	size_t i = 0;
	char **atoi_testing_values = malloc(sizeof(char *) * 8);
	atoi_testing_values[0] = "120";
	atoi_testing_values[1] = "0";
	atoi_testing_values[2] = "+233";
	atoi_testing_values[3] = "-93";
	atoi_testing_values[4] = "2147483647";
	atoi_testing_values[5] = "-2147483648";
	atoi_testing_values[6] = "+-42";
	atoi_testing_values[7] = 0;
	while (atoi_testing_values[i])
	{
		printf("%s => %d\n", 
			atoi_testing_values[i],
			ft_atoi(atoi_testing_values[i])
		);
		i++;
	}
	free(atoi_testing_values);
}

void test_bzero()
{
	printf("------------------------------------------------------- FT_BZERO\n");
	size_t	index = 0;
	// One 
	char *string_one = malloc(sizeof(char) * 40000);
	ft_bzero(string_one, 10000);
	while (!string_one[index])
		index++;
	printf("(\"testing\", 10_000) => %zu\n", index);
	free(string_one);
	// Two 
	index = 0;
	char *string_two = strdup("testing");
	ft_bzero(string_two, 0);
	while (!string_two[index])
		index++;
	printf("(\"testing\", 0)  => %zu\n", index);
	free(string_two);
	// Three
	index = 0;
	char *string_three = strdup("");
	ft_bzero(string_three, 4);
	while (!string_three[index])
		index++;
	printf("(\"\", 4) => %zu\n", index);
	free(string_three);
}

